    /** Defines callbacks for service binding, passed to bindService() */
    private ServiceConnection mConnection = new ServiceConnection() {

        @Override
        public void onServiceConnected(ComponentName className,
                                       IBinder service) {
            // We've bound to LocalService, cast the IBinder and get LocalService instance
            DownloadService.DownloadServiceBinder binder =
                    (DownloadService.DownloadServiceBinder)service;

            mService = binder.getService();
            mBound = true;
        }

        @Override
        public void onServiceDisconnected(ComponentName arg0) {
            mBound = false;
        }
    };

    ArrayList<String> getSavedImages(String path) {
        File f = new File(path);
        File file[] = f.listFiles();

        ArrayList<String> images = new ArrayList<String>(file.length);

        for (int i = 0; i < file.length; i++) {
            if (file[i].exists())
                images.add(file[i].getAbsolutePath());
        }

        return images;
    }
